#include <stdio.h>
#include "3-calc.h"

/**
* op_add- returns the sum of to integers
* @a: first parameter
* @b: second parameter
*
* Return: sum of the integers.
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_add- returns the difference of a and b
* @a: first parameter
* @b: second parameter
*
* Return: difference of the integers.
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_add- returns the product of a and b
* @a: first parameter
* @b: second parameter
*
* Return: product of the integers.
*/
int op_mul(int a, int b);
{
	return (a * b);
}

/**
* op_add- returns the division of a by b
* @a: first parameter
* @b: second parameter
*
* Return: division of the integers.
*/
int op_div(int a, int b)
{
	return (a / b);
}

 /**
 * op_add- returns the remainder of the divisior of a by b
 * @a: first parameter
 * @b: second parameter
 *
 * Return: remainder of the division of the integers.
 */
 int op_mod(int a, int b)
 {
 	return (a % b);
 }
